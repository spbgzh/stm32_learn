/**********SLEEP IN***************/
void EnterSleep(void)
{
	LCD_WR_REG(0x28);
	LCD_WR_REG(0x10);
	HAL_Delay(120);
}

/**********SLEEP OUT***************/
void ExitSleep(void)
{
	LCD_WR_REG(0x11);
	HAL_Delay(120);
	LCD_WR_REG(0x29);
	HAL_Delay(10);
}

